
public class cualquiercosa {
	public cualquiercosa(int hola, int num) {
		super();
		this.hola = hola;
		this.num = num;
	}
	private int hola;
	private int num;
	public int getHola() {
		return hola;
	}
	public void setHola(int hola) {
		this.hola = hola;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
